public class delenie {
    public static void main(String[] args) {
        int q = 182;
        int w = 82;

        int целая_часть = q / w;
        int остаток_от_деления = q % w;

        System.out.println("Результат деления числа q=182 на число w=82: " +целая_часть+ "." +остаток_от_деления);
    }
}

